//! GECCO marginal conformance tests.
//!
//! Verifies that marginal probabilities from the Rust library API match
//! the original C CRFsuite binary when using the real GECCO biosynthetic
//! gene cluster model.  This catches regressions that would cause the Rust
//! reimplementation to over- or under-predict cluster probabilities.
//!
//! Test data: three 20-gene windows from CP157504.1 (Streptomyces), each
//! exercising a different probability range:
//!   - window_start:   near-zero probabilities (non-cluster region)
//!   - window_cluster: rising probabilities (entering a BGC)
//!   - window_mid:     mixed low/medium probabilities
//!
//! Ground truth generated by sklearn-crfsuite 0.5.0 (Python wrapper around
//! the original C CRFsuite library) and cross-checked against the C binary.

#![allow(clippy::excessive_precision)]

