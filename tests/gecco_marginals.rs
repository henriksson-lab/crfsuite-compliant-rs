// ── helpers ────────────────────────────────────────────────────────────────

fn project_root() -> PathBuf {
    PathBuf::from(env!("CARGO_MANIFEST_DIR"))
}

fn rust_bin() -> PathBuf {
    if let Some(bin) = option_env!("CARGO_BIN_EXE_crfsuite-compliant-rs") {
        return PathBuf::from(bin);
    }
    project_root().join("target/debug/crfsuite-compliant-rs")
}

fn test_data(name: &str) -> PathBuf {
    project_root().join("test_data").join(name)
}

fn gecco_model_path() -> PathBuf {
    test_data("gecco_model.crfsuite")
}

fn gecco_model_bytes() -> Option<Vec<u8>> {
    let path = gecco_model_path();
    if !path.exists() {
        eprintln!("SKIP: test_data/gecco_model.crfsuite missing");
        return None;
    }
    Some(std::fs::read(path).expect("read GECCO model"))
}

fn gecco_iwa_path(name: &str) -> Option<PathBuf> {
    let path = test_data(name);
    if !path.exists() {
        eprintln!("SKIP: {name} missing");
        return None;
    }
    Some(path)
}

/// Build an Instance from a list of feature-name lists, the way gecco-rs does.
/// Each inner Vec<&str> is the set of Pfam domain names present for one gene.
fn build_instance(model: &ModelReader, features: &[Vec<&str>]) -> Instance {
    let items: Vec<Item> = features
        .iter()
        .map(|feat_list| {
            let attrs: Vec<Attribute> = feat_list
                .iter()
                .filter_map(|name| model.to_aid(name).map(|aid| Attribute { aid, value: 1.0 }))
                .collect();
            Item { contents: attrs }
        })
        .collect();

    Instance {
        items,
        labels: vec![0i32; features.len()],
        weight: 1.0,
        group: 0,
    }
}

/// Get marginal P(label="1" | x) for each position via the library API.
fn library_marginals(model: &ModelReader, features: &[Vec<&str>]) -> Vec<f64> {
    let mut tagger = Crf1dTagger::new(model);
    let inst = build_instance(model, features);
    tagger.set(&inst);

    let label_1 = model.to_lid("1").expect("model must have label '1'");
    (0..features.len())
        .map(|t| tagger.marginal_point(label_1, t as i32))
        .collect()
}

fn canonical_library_marginals(model: &ModelReader, features: &[Vec<&str>]) -> Vec<f64> {
    let mut tagger = Crf1dTagger::new(model);
    let inst = build_instance(model, features);
    tagger.set_canonical(&inst);

    let label_1 = model.to_lid("1").expect("model must have label '1'");
    (0..features.len())
        .map(|t| tagger.marginal_point(label_1, t as i32))
        .collect()
}

/// Parse marginals from C/Rust CLI `-l` output.
/// Each line looks like: `0\t0:0.999998\t1:0.000002`
fn parse_cli_marginals(output: &str) -> Vec<f64> {
    output
        .lines()
        .filter(|line| !line.is_empty() && !line.starts_with('@'))
        .map(|line| {
            // Find the field that starts with "1:"
            line.split('\t')
                .find_map(|field| field.strip_prefix("1:").map(|v| v.parse::<f64>().unwrap()))
                .expect("no 1:value field in CLI output line")
        })
        .collect()
}

