// ── CLI cross-validation tests ─────────────────────────────────────────────

macro_rules! skip_without_c {
    () => {
        let c = project_root().join("crfsuite/frontend/.libs/crfsuite");
        if !c.exists() {
            eprintln!("SKIP: C crfsuite binary not found");
            return;
        }
    };
}

fn run_cli(bin: &PathBuf, model: &str, iwa: &str, env: Option<(&str, PathBuf)>) -> String {
    let mut cmd = Command::new(bin);
    cmd.args(["tag", "-m", model, "-l", iwa]);
    if let Some((key, val)) = env {
        cmd.env(key, val);
    }
    let o = cmd.output().expect("spawn binary");
    assert!(
        o.status.success(),
        "{bin:?} failed: {}",
        String::from_utf8_lossy(&o.stderr)
    );
    String::from_utf8(o.stdout).unwrap()
}

/// Verify Rust CLI and C CLI produce identical marginals on the GECCO model.
#[test]
fn gecco_cli_marginals_vs_c() {
    skip_without_c!();

    let c_bin = project_root().join("crfsuite/frontend/.libs/crfsuite");
    let rs_bin = rust_bin();
    assert!(rs_bin.exists(), "Rust binary missing – run `cargo build`");

    let model_path = gecco_model_path();
    if !model_path.exists() {
        eprintln!("SKIP: test_data/gecco_model.crfsuite missing");
        return;
    }
    let model = model_path.to_str().unwrap().to_string();
    let lib_path = project_root().join("crfsuite/lib/crf/.libs");

    for window_name in ["window_start", "window_cluster", "window_mid"] {
        let Some(iwa_path) = gecco_iwa_path(&format!("gecco_iwa_{window_name}.txt")) else {
            return;
        };
        let iwa = iwa_path.to_str().unwrap().to_string();

        let c_out = run_cli(
            &c_bin,
            &model,
            &iwa,
            Some(("LD_LIBRARY_PATH", lib_path.clone())),
        );
        let rs_out = run_cli(&rs_bin, &model, &iwa, None);

        assert_eq!(c_out, rs_out, "CLI marginals differ for {window_name}");
    }
}

/// Verify library API matches CLI output for the GECCO model.
/// This catches bugs in how the library is called (Instance construction,
/// attribute mapping, etc.) vs the CLI's IWA parsing path.
#[test]
fn gecco_library_vs_cli_marginals() {
    let rs_bin = { rust_bin() };
    if !rs_bin.exists() {
        eprintln!("SKIP: Rust binary not built");
        return;
    }

    let model_path = gecco_model_path();
    if !model_path.exists() {
        eprintln!("SKIP: test_data/gecco_model.crfsuite missing");
        return;
    }
    let model_str = model_path.to_str().unwrap().to_string();
    let data = std::fs::read(&model_path).unwrap();
    let model = ModelReader::open(&data).unwrap();

    let cases: Vec<(&str, Vec<Vec<&str>>)> = vec![
        ("window_start", window_start_features()),
        ("window_cluster", window_cluster_features()),
        ("window_mid", window_mid_features()),
    ];

    for (name, features) in &cases {
        // Library API
        let lib_marginals = library_marginals(&model, features);

        // CLI
        let Some(iwa_path) = gecco_iwa_path(&format!("gecco_iwa_{name}.txt")) else {
            return;
        };
        let iwa = iwa_path.to_str().unwrap().to_string();
        let cli_out = run_cli(&rs_bin, &model_str, &iwa, None);
        let cli_marginals = parse_cli_marginals(&cli_out);

        assert_eq!(
            lib_marginals.len(),
            cli_marginals.len(),
            "{name}: length mismatch"
        );
        for (t, (lib_val, cli_val)) in lib_marginals.iter().zip(cli_marginals.iter()).enumerate() {
            let diff = (lib_val - cli_val).abs();
            // CLI rounds to 6 decimals, so allow 5e-7 tolerance
            assert!(
                diff < 5e-7,
                "{name} position {t}: library={lib_val:.10e}, cli={cli_val:.6}, diff={diff:.2e}"
            );
        }
    }
}

#[test]
fn gecco_canonical_marginals_are_attribute_order_independent() {
    let Some(data) = gecco_model_bytes() else {
        return;
    };
    let model = ModelReader::open(&data).unwrap();
    let mut features = window_start_features();
    let baseline = canonical_library_marginals(&model, &features);

    for item in &mut features {
        item.reverse();
    }
    let reversed = canonical_library_marginals(&model, &features);

    assert_eq!(baseline, reversed);
}
