// ── library API tests ──────────────────────────────────────────────────────

/// Assert library marginals match expected values within tolerance.
/// Uses relative tolerance for large values, absolute for near-zero.
fn assert_marginals_close(actual: &[f64], expected: &[f64], label: &str) {
    assert_eq!(actual.len(), expected.len(), "{label}: length mismatch");
    for (t, (a, e)) in actual.iter().zip(expected.iter()).enumerate() {
        let abs_diff = (a - e).abs();
        let rel_diff = if *e != 0.0 {
            abs_diff / e.abs()
        } else {
            abs_diff
        };
        // Allow 1e-4 relative or 1e-15 absolute (for near-zero values)
        assert!(
            rel_diff < 1e-4 || abs_diff < 1e-15,
            "{label} position {t}: got {a:.10e}, expected {e:.10e} (rel={rel_diff:.2e}, abs={abs_diff:.2e})"
        );
    }
}

